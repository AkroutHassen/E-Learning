function noms($diplomes){
      $nom = [];
      
      foreach ($diplomes as $diplome) {
          $nom[$diplome->id] = $diplome->nom;
      
      }
      

      return $nom;
  }

 function nbVarPVar($cours,$diplomes){
        $count = [];
        foreach ($diplomes as $diplome) {
            $count[$diplome->id] = 0;
            foreach($cours as $cour){
                if($cour->codeDip == $diplome->id){
                    $count[$diplome->id]++;
                }
            }
        }
    
        return $count;
    }
